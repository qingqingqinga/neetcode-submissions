class Solution:
    def reverseBits(self, n: int) -> int:
        res = 0
        for i in range(32):
            
            # ==========================================================
            # 第一步：从原数 n 中，取出第 i 位的值（只能是 0 或 1）
            # ==========================================================
            # 1. n >> i   : 把原数 n 向右移动 i 位。
            #    原数第 i 位被移动到了最右边的位置（第 0 位）。
            #    举例：n = 1010, i = 1, n >> 1 = 0101 (原来的第1位 '1' 现在在最右边)
            # 2. & 1      : 和数字 1 做按位与运算。
            #    这样会清除掉除了最右边一位以外的所有位，只留下我们想取的那个 bit。
            bit = (n >> i) & 1

            # ==========================================================
            # 第二步：把取出来的这位 bit，放到结果 res 的镜像位置
            # ==========================================================
            # 1. bit << (31 - i) : 将 bit（0或1）向左移动 (31 - i) 位。
            #    当 i=0（原数最右边），(31-0)=31，bit 被移到最左边（第31位）。
            #    当 i=31（原数最左边），(31-31)=0，bit 不动，留在最右边（第0位）。
            #    这正好实现了“镜像反转”。
            # 
            # 2. res | (中间数)   : 按位或运算。
            #    因为 res 其他位置已经有之前循环填好的 1，用 | 可以保证
            #    只把当前这一位设为 1（或保持不变），不会破坏其他位置的数据。
            res = res | (bit << (31 - i))

        
        return res